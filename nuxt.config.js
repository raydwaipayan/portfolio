const path = require('path')
const glob = require('glob')
const files = glob.sync('**/*.md', { cwd: 'blog' })

function getBlog (post, _) {
  const blog = post.substr(0, post.lastIndexOf('.'))
  return `/blog/${blog}`
}

const routerBase = process.env.DEPLOY_ENV === 'GH_PAGES' ? {
  router: {
    base: '/portfolio'
  }
} : {}

